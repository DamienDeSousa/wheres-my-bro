import Head from 'next/head'
import { signOut, useSession } from 'next-auth/react'
import { Presentation } from '@/components/Presentation'
import { fetcher } from '@/services/fetcher'
import useSWR from 'swr'
import { FirstConnexion } from '@/components/FirstConnexion'
import { AvailableBros } from '@/components/AvailableBros'

export default function Home() {
  const { data: session } = useSession()
  const { data, error } = useSWR('/api/userAccount', fetcher)

  console.log(data)
  // fond beige, texte noir, titre jaune / rouge / orange,
  return (
    <>
      <Head>
        <title>Where's my BRO ?</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div
        style={{
          minHeight: '100vh',
          display: 'flex',
          ...(!session && { alignItems: 'center' }),
        }}
      >
        {session ? (
          data?.isFirstConnexion ? (
            <div>
              <button onClick={() => signOut()}>Sign out</button>
              <FirstConnexion />
            </div>
          ) : (
            <div>
              <button onClick={() => signOut()}>Sign out</button>
              <AvailableBros />
            </div>
          )
        ) : (
          <Presentation />
        )}
      </div>
    </>
  )
}
